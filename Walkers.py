import cv2

# Load the cascade classifier file
body_classifier = cv2.CascadeClassifier('C:/Users/risho/Downloads/PRO-106-ProjectTemplate-main/PRO-106-ProjectTemplate-main/haarcascade_fullbody.xml')

# Start capturing video from the webcam
video_capture = cv2.VideoCapture(0)

while True:
    # Read the current frame
    ret, frame = video_capture.read()

    # Convert the frame to grayscale
    gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)

    # Detect bodies in the frame
    bodies = body_classifier.detectMultiScale(gray, scaleFactor=1.1, minNeighbors=5)

    # Draw rectangles around the detected bodies
    for (x, y, w, h) in bodies:
        cv2.rectangle(frame, (x, y), (x + w, y + h), (0, 255, 0), 3)




















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































    # Display the frame
    cv2.imshow('Body Detection', frame)

